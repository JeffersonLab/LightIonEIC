*DECK F2X
      PROGRAM F2X
C
C     PACKAGE TAG -- DEUTERON DIS WITH SPECTATOR TAGGING
C     AUTHOR C. WEISS (WEISS.AT.JLAB.ORG)
C
C     APPLICATION PROGRAM
C
C     TABULATE NUCLEON STRUCTURE FUNCTION F2 IN X
C
C     V1 13MAY14
C
      IMPLICIT DOUBLE PRECISION (A - H, O - Z)
C
      OPEN(1, FILE = 'F2X.IN',   STATUS = 'OLD')
      OPEN(2, FILE = 'F2X.OUT',  STATUS = 'OLD')
      REWIND(1)
      REWIND(2)
C
C     ...READ INPUT PARAMETERS (DOCUMENTATION SEE FILE)
C
      READ(1, *) QQ
      READ(1, *) DL1
      READ(1, *) DL2
      READ(1, *) NSTEP
C
C     ...WRITE OUTPUT FILE HEADER
C
      WRITE(2, 91000) 'OUTPUT STRUX'
      WRITE(2, 91000) 'TABULATE STRUCTURE FUNCTION IN X'
      WRITE(2, 91000)
      WRITE(2, 91001) QQ, 'QQ'
      WRITE(2, 91000)
      WRITE(2, 91000) 'COL1  X'
      WRITE(2, 91000) 'COL2  F2, PROTON'
      WRITE(2, 91000) 'COL3  F2, NEUTRON'
91000 FORMAT('#*',                A)
91001 FORMAT('#', T3, F12.6, T20, A)
91002 FORMAT('#', T3, I12,   T20, A)
C
      DO 10000 I = 0, NSTEP
C
         DL = DL1 + (I*(DL2 - DL1))/NSTEP
         X = 10**DL
C
C        ...STRUCTURE FUNCTION
C
         CALL TAGFN(F2P, X, QQ, 1)
         CALL TAGFN(F2N, X, QQ, 2)
C
C        PRINT 90000,    X, F2P, F2N
         WRITE(2, 90000) X, F2P, F2N
90000    FORMAT(F10.5, 4(1X, E12.6))
C
10000 CONTINUE
C
      END
C
